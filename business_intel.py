from ai import ask_ai


def analyze_market(query: str) -> str:
    prompt = f"""
Ты AI-бизнес-аналитик и маркетолог.

Пользователь хочет получить бизнес-разведку по запросу:
{query}

Сделай аналитический отчет. Важно: не утверждай, что ты реально нашел конкретные сайты, цены или отзывы в интернете.

Пиши как эксперт по рынку и продвижению.

Структура ответа:

🕵️ AI-бизнес-разведка

1. Что обычно происходит в этой нише
2. Какие конкуренты чаще всего встречаются
3. Какие офферы обычно используют
4. Где такие компании теряют клиентов
5. Что можно улучшить в сайте и рекламе
6. Какие идеи продвижения можно протестировать
7. Как выделиться среди конкурентов
8. Что можно автоматизировать через Hitly
9. Следующие шаги

Пиши на русском, коротко, структурно и по делу.
Не обещай гарантированный рост продаж.
В конце предложи пройти AI-аудит бизнеса, анализ сайта или оставить заявку на консультацию.
"""
    return ask_ai(prompt)
