from ai import ask_ai
from site_parser import fetch_site_text


def analyze_site(site_url: str) -> str:
    try:
        site_text = fetch_site_text(site_url)
    except Exception as e:
        return (
            "Не удалось открыть сайт для анализа.\n\n"
            "Проверьте, что ссылка начинается с https:// и сайт доступен.\n\n"
            f"Техническая ошибка: {e}"
        )

    prompt = f"""
Ты эксперт по сайтам, продажам и автоматизации бизнеса.

Проанализируй именно этот сайт по тексту ниже.

Сайт: {site_url}

Данные сайта:
{site_text}

Сделай конкретный разбор:

1. Что понятно с первого экрана
2. Насколько ясно предложение
3. Где сайт может терять заявки
4. Какие элементы доверия есть или отсутствуют
5. Насколько легко оставить заявку
6. Что можно автоматизировать через Hitly
7. Что улучшить в первую очередь

Не пиши общие фразы. Ссылайся на то, что видно в тексте сайта.
Пиши коротко, по делу, на русском.
"""

    return ask_ai(prompt)
