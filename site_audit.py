from ai import ask_ai


def analyze_site(site_url: str) -> str:
    prompt = f"""
Ты AI-эксперт по сайтам, продажам и автоматизации бизнеса.

Пользователь отправил сайт:
{site_url}

Сделай первичный аудит сайта. Важно: не утверждай, что ты реально открыл сайт.
Пиши как эксперт, который оценивает сайт по бизнес-логике.

Структура ответа:

🌐 AI-анализ сайта

1. Что нужно проверить на сайте
2. Где сайт может терять заявки
3. Что мешает клиенту оставить заявку
4. Что можно автоматизировать через Hitly
5. Что улучшить в первую очередь
6. Рекомендация

Фокус:
- заявки;
- скорость ответа клиенту;
- формы связи;
- доверие;
- понятность оффера;
- повторные касания;
- AI-консультант;
- автоматизация продаж.

Пиши на русском, коротко и по делу.
В конце предложи пройти AI-аудит бизнеса или оставить заявку на консультацию.
"""
    return ask_ai(prompt)
